package com.win.radio.manila.utilities;

public interface DramaCommands {

	/* JSP SCRIPTS */

	public String GET_ALL_DRAMA = "SELECT DATE_FORMAT(T.CREATE_DATE, '%b %d, %Y %h:%i') as 'CREATE_DATE', " + 
			"CONCAT(A.FIRST_NAME, ' ', A.LAST_NAME, ' ', T.DESCRIPTION) AS 'EVENT' " + 
			"FROM TRANSACTION_LOG T " + 
			"LEFT JOIN ACCOUNT A ON T.UPDATE_USER = A.ID_ACCOUNT " + 
			"ORDER BY ID_TRANSACTION DESC LIMIT 4";
		
	public String GET_ALL_EPISODES = "SELECT DATE_FORMAT(E.CREATE_DATE, '%b %d, %Y %h:%i') AS DATE_POSTED, "
			+ "DATE_FORMAT(E.UPDATE_DATE, '%b %d, %Y %h:%i') AS UPDATE_DATE, E.ID_EPISODE, A.USERNAME, "
			+ "E.TITLE, E.DESCRIPTION, E.FILENAME, E.IMAGE, E.STATUS FROM EPISODE AS E "
			+ "LEFT JOIN ACCOUNT A ON E.UPDATE_USER = A.ID_ACCOUNT WHERE E.COD_REGION = ? AND E.STATUS LIKE ? "
			+ "ORDER BY E.ID_EPISODE DESC";

	public String GET_DRAMA_DETAILS = "SELECT ID_EPISODE, TITLE, DESCRIPTION, FILENAME, IMAGE, STATUS FROM EPISODE WHERE ID_EPISODE = ?";

	/* CONTROLLER SCRIPTS */

	public String ADD_DRAMA = "INSERT INTO EPISODE (CREATE_DATE, UPDATE_DATE, UPDATE_USER, TITLE, DESCRIPTION, "
			+ "FILENAME, IMAGE, STATUS, COD_REGION) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)";

	public String UPDATE_DRAMA = "UPDATE EPISODE SET UPDATE_DATE = ?, UPDATE_USER = ?, TITLE = ?, DESCRIPTION = ?, FILENAME = ?, IMAGE = ? "
			+ "WHERE ID_EPISODE = ?";

	public String UPDATE_EPISODE_STATUS = "UPDATE EPISODE SET UPDATE_DATE = ?, "
			+ "UPDATE_USER = ?, STATUS = ? WHERE ID_EPISODE = ?";


}
